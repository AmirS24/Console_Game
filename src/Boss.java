public class Boss {
    private int healBoss;
    private  int damageBoss;
    private String typeOfDefence;
    public int getHealBoss(){
        return healBoss;
    }
    public int getDamageBoss(){
        return damageBoss;
    }
    public String  getTypeOfDefence(){
        return typeOfDefence;
    }
    public void setHealBoss(int healBoss){
this.healBoss = healBoss;
    }
    public void setDamageBoss(int damageBoss){
this.damageBoss = damageBoss;
    }
    public void setTypeOfDefence(String typeOfDefence){
this.typeOfDefence = typeOfDefence;
    }

}
